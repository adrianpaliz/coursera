# Operations can be: matematical, logical and comparison
'''
Matematical
Plus sign + for addition
Minus sign - to subtraction
Forward slash / for division
Asterisk * for multiplication
'''
'''
print(2 + 2)
print(2 - 2)
print(35 / 5)
print(35 * 5)
'''
'''
Logical
'and' Checks for both conditions to be true
'or' Checks for at least one conditions to be true
'not' Returns false if the result is true
'''
a = False
b = False
if a and b:
    print('All true!')

if a or b:
    print('All true!')

if not(a) or not(b):
    print('All true')


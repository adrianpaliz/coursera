# Modules
# Are similar to files
# Generally are written in a single file
# Can contain clases, functions and data members just like any other Python file.

# Packages
# Are like directories that contain different files
# Are a type of module
# Any module that contains the __path__ definition is a package
# Can contain sub-packages and other modules.

# Library
# Is a term that's used interchangeably with importes packages.
# It refers to a collection of packages.

